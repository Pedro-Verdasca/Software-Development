print(f"a ** b = {soma}")
print(f"a + b = {subtração}")
print(f"a - b = {multiplicacao}")
print(f"a * b = {divisao}")
print(f"a / b = {div_inteira}")
print(f"a // b = {modulo}")
print(f"a % b = {potencia}")


